    <h1>About Mapping the Fourth</h1>
    <br>
    <p>
      Mapping the Fourth is a crowdsourced digital archive of primary sources that reveal how Americans celebrated July 4 during the Civil War era. It will bring together thousands of documents, ranging from newspaper articles and speeches to private letters and diaries. These sources reveal how a wide range of Americans — northern and southern, white and black, male and female, Democrat and Republican, immigrant and native born — all used the Fourth to articulate their deepest beliefs about American identity during the great crisis of the Civil War.<br>
      <br>
      We invite anyone interested in Civil War era history to explore the site. As well as simply learning more about how July 4 was celebrated in your hometown, you can also contribute to the project by transcribing, tagging, connecting, and discussing different sources and themes.<br>
      <br>
      While Mapping the Fourth can be used by anyone, it is especially useful as a teaching tool. Whether you teach at the college or high school level, your students will jump at the chance to learn about how a previous generation of Americans celebrated the Fourth. (Yes, there were fireworks!) These are engaging documents that open up big themes: North-South differences; the causes and consequences of the Civil War; African American experiences of emancipation. On our website you’ll find standards-based assignment guidelines that make it easy to integrate it into your courses.<br>
      <br>
      The site uses the Omeka content management system. Our team has created an open-source plugin, Incite, that allows users to work with Omeka databases in new ways—transcribing, tagging, connecting, and discussing. We’re making it easier than ever to become a citizen archivist! Incite will be freely available for other Omeka-based projects.<br>
      <br>
      Coordinated by the Virginia Center for Civil War Studies, Mapping the Fourth involves faculty, staff, and support from around Virginia Tech: the Center for Human-Computer Interaction, History, Computer Science, the School of Education, and the University Libraries. The project is funded with a “Literacy and Engagement with Historical Records” grant from the National Archives’ National Historical Publications and Records Commission.<br>
      <br><br>
